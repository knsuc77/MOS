public class BBS {

	private String bbsTitle;
	private String bbsContext;
	private String bbsAttached;
	private String bbsType;
	public String getBbsType() {
		return bbsType;
	}
	public void setBbsType(String bbsType) {
		this.bbsType = bbsType;
	}
	public String getBbsTitle() {
		return bbsTitle;
	}
	public void setBbsTitle(String bbsTitle) {
		this.bbsTitle = bbsTitle;
	}
	public String getBbsContext() {
		return bbsContext;
	}
	public void setBbsContext(String bbsContext) {
		this.bbsContext = bbsContext;
	}
	public String getBbsAttached() {
		return bbsAttached;
	}
	public void setBbsAttached(String bbsAttached) {
		this.bbsAttached = bbsAttached;
	}
	
	
}
